export const normalizeStateName = (inputName) => {
  if (!inputName) return '';
  const lower = inputName.toLowerCase().trim();
  
  if (lower.includes('andaman')) return 'Andaman and Nicobar Islands';
  if (lower.includes('nicobar')) return 'Andaman and Nicobar Islands';
  if (lower.includes('chandigarh')) return 'Chandigarh';
  if (lower.includes('dadra') || lower.includes('daman')) return 'Dadra and Nagar Haveli and Daman and Diu';
  if (lower.includes('jammu') || lower.includes('kashmir') || lower.includes('srinagar')) return 'Jammu and Kashmir';
  if (lower.includes('ladakh') || lower.includes('leh')) return 'Ladakh';
  if (lower.includes('lakshadweep') || lower.includes('kavaratti')) return 'Lakshadweep';
  if (lower.includes('delhi')) return 'Delhi';
  if (lower.includes('pondicherry') || lower.includes('puducherry')) return 'Puducherry';
  
  return inputName;
};
